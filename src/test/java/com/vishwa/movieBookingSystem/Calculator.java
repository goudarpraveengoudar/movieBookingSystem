package com.vishwa.movieBookingSystem;

public class Calculator {
    public int add(int a, int b){
        return a+b;
    }
    public int sub(int a, int b){
        return a-b;
    }
    public int division(int a, int b){
        return a/b;
    }
    public int  multiplication(int a,int b){
        return a*b;
    }
}
